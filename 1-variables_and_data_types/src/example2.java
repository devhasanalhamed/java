public class example2 {

    public static void main(String[] args) {

        String x = "dev hasan alhamed";
        System.out.println("I am " + x);
        System.out.println("I am American citizen");
        System.out.print("I am married \n");
        System.out.println("I am a dream catcher");
        // java is a case-sensitive
        // every statement ends with semicolon;


        // we need to declare variable: datatype and name
        // initialize variable with a value

        int score;
        score = 6;
        System.out.println(score);
        score = 10;
        System.out.println(score);
        score += score;
        System.out.println(score);
        score++;
        System.out.println(score);

        int apples = 3;
        int oranges = 2;

        System.out.println("apples: " + apples + " oranges: " + oranges);
        System.out.println("fruits: " + apples + oranges);

        System.out.println(5 * 8);
        System.out.println(5 + 8);
    }


}
